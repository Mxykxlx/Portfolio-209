//sa
function opensa(imageSrc) {
    var modalsa = document.getElementById('myModalsa');
    var modalImgsa = document.getElementById('modalImgsa');
    modalsa.style.display = 'block';
    modalImgsa.src = imageSrc;
  }
  
  function closeModalsa() {
    var modal = document.getElementById('myModalsa');
    modal.style.display = 'none';
  }
  




